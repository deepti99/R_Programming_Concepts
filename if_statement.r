# R program to illustrate
# if statement
a <- 76
b <- 67

# TRUE condition
if(a > b)
{
  c <- a - b
  print("condition a > b is TRUE")
  print(paste("Difference between a, b is : ", c))
}

# FALSE condition
if(a < b)
{
  c <- a - b
  print("condition a < b is TRUE")
  print(paste("Difference between a, b is : ", c))
}
